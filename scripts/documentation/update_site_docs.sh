# Update project documentation index
update_documentation_index() {
    local action="$1"  # "add" or "remove"
    local domain="$2"
    local site_path="$3"
    
    local docs_index="$PROJECT_ROOT/docs/sites-index.md"
    
    if [[ "${DRY_RUN:-false}" == "true" ]]; then
        log_info "[DRY RUN] Would update documentation index for $action: $domain"
        return 0
    fi
    
    # Create documentation index if it doesn't exist
    if [[ ! -f "$docs_index" ]]; then
        cat > "$docs_index" << EOF
# JStack - Sites Documentation Index

Generated: $(date)
Last Updated: $(date)

## Managed Sites

This document provides an index of all sites managed by this JStack instance.

EOF
    fi
    
    case "$action" in
        "add")
            # Add site to index
            cat >> "$docs_index" << EOF

### $domain

- **Domain**: $domain
- **Site Path**: $site_path
- **Added**: $(date)
- **Status**: Active
- **Configuration**: [$site_path/site.json]($site_path/site.json)
- **README**: [$site_path/README.md]($site_path/README.md)
- **Compliance Profile**: ${DEFAULT_COMPLIANCE_PROFILE:-default}

**Service URLs**:
- Supabase API: https://${SUPABASE_SUBDOMAIN:-supabase}.$domain
- Supabase Studio: https://${STUDIO_SUBDOMAIN:-studio}.$domain  
- N8N Workflows: https://${N8N_SUBDOMAIN:-n8n}.$domain

EOF
            log_success "Added $domain to documentation index"
            ;;
        "remove")
            # Remove site from index (mark as removed)
            if grep -q "### $domain" "$docs_index"; then
                # Create temporary file with site marked as removed
                local temp_index=$(mktemp)
                sed "/### $domain/,/^$/c\\
### $domain (REMOVED)\\
\\
- **Domain**: $domain\\
- **Removed**: $(date)\\
- **Status**: Removed\\
- **Archive Date**: $(date)\\
" "$docs_index" > "$temp_index"
                
                mv "$temp_index" "$docs_index"
                log_success "Marked $domain as removed in documentation index"
            else
                log_warning "Site $domain not found in documentation index"
            fi
            ;;
    esac
    
    # Update last modified timestamp
    sed -i "s/Last Updated:.*/Last Updated: $(date)/" "$docs_index"
}

