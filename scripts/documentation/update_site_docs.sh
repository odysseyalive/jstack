# Update all documentation for a site action
update_site_documentation() {
    local action="$1"  # "add" or "remove"
    local domain="$2"
    local site_path="${3:-/sites/$domain}"
    
    log_section "Updating Documentation for $action: $domain"
    
    case "$action" in
        "add")
            # Create site directory if it doesn't exist
            if [[ ! -d "$site_path" ]]; then
                log_info "Creating site directory: $site_path"
                mkdir -p "$site_path"
            fi
            
            # Generate all documentation templates
            generate_site_readme_template "$domain" "$site_path"
            generate_site_config_template "$domain" "$site_path" "${DEFAULT_COMPLIANCE_PROFILE:-default}"
            generate_deployment_guide "$domain" "$site_path"
            
            # Update project documentation index
            update_documentation_index "add" "$domain" "$site_path"
            
            log_success "Documentation generated for new site: $domain"
            ;;
        "remove")
            # Update project documentation index
            update_documentation_index "remove" "$domain" "$site_path"
            
            log_success "Documentation updated for removed site: $domain"
            ;;
        *)
            log_error "Unknown documentation action: $action"
            return 1
            ;;
    esac
}

# Show help information
show_help() {
    cat << EOF
JStack Documentation Update System

USAGE:
    bash update_site_docs.sh [COMMAND] [OPTIONS]

COMMANDS:
    add-site DOMAIN [PATH]      Generate documentation for new site
    remove-site DOMAIN [PATH]   Update documentation for removed site
    update-index                Rebuild documentation index
    help                        Show this help message

EXAMPLES:
    # Generate documentation for new site
    bash update_site_docs.sh add-site example.com /sites/example.com

    # Update documentation for removed site
    bash update_site_docs.sh remove-site example.com

    # Rebuild documentation index
    bash update_site_docs.sh update-index

GENERATED FILES:
    - README.md              Site overview and configuration guide
    - site.json             Site configuration template
    - DEPLOYMENT.md         Complete deployment guide
    - docs/sites-index.md   Project-wide sites index

EOF
}

# Main execution
main() {
    local command="${1:-help}"
    
    case "$command" in
        "add-site")
            if [[ -z "$2" ]]; then
                log_error "Domain is required for add-site command"
                show_help
                exit 1
            fi
            update_site_documentation "add" "$2" "$3"
            ;;
        "remove-site")
            if [[ -z "$2" ]]; then
                log_error "Domain is required for remove-site command"
                show_help
                exit 1
            fi
            update_site_documentation "remove" "$2" "$3"
            ;;
        "update-index")
            log_info "Rebuilding documentation index..."
            # This would scan all sites and rebuild the index
            log_info "Index rebuild functionality would be implemented here"
            ;;
        "help"|"-h"|"--help")
            show_help
            ;;
        *)
            log_error "Unknown command: $command"
            show_help
            exit 1
            ;;
    esac
}

# Execute main function if script is run directly
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi